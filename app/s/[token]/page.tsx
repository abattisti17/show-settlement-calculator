    ticketTiers?: { name: string; price: string; sold: string; comps?: string }[];
    capacity?: string;
    ticketPrice?: string;
    ticketsSold?: string;
    taxRate?: string;
    taxMode?: string;
    ccFeeRate?: string;
    ccFeeMode?: string;
    totalExpenses?: string;
    expenseItems?: { label: string; amount: string; note?: string }[];
    notes?: string;
