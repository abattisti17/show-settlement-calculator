    guarantee?: string;
    percentage?: string;
    breakeven?: string;
    deposit?: string;
    withholdingRate?: string;
    withholdingState?: string;
    buyoutItems?: { label: string; amount: string }[];
    buyoutMode?: string;
    merchGross?: string;
